static uint16_t sinLUT[] = {
	0, 143, 286, 429, 571, 714, 856, 998, 1140, 1282, 1423, 
	1563, 1703, 1843, 1982, 2120, 2258, 2395, 2531, 2667, 2802, 
	2936, 3069, 3201, 3332, 3462, 3591, 3719, 3846, 3972, 4096, 
	4219, 4341, 4462, 4581, 4699, 4815, 4930, 5043, 5155, 5266, 
	5374, 5482, 5587, 5691, 5793, 5893, 5991, 6088, 6183, 6275, 
	6366, 6455, 6542, 6627, 6710, 6791, 6870, 6947, 7022, 7094, 
	7165, 7233, 7299, 7363, 7424, 7484, 7541, 7595, 7648, 7698, 
	7746, 7791, 7834, 7875, 7913, 7949, 7982, 8013, 8041, 8068, 
	8091, 8112, 8131, 8147, 8161, 8172, 8181, 8187, 8191, 8192
};

void getSinCos(int16_t angle, int16_t *sin, int16_t *cos) {
	if(angle >= 270) {
		*sin = -sinLUT[360 - angle];
		*cos =  sinLUT[angle - 270];
	} else if(angle >= 180) {
		*sin = -sinLUT[angle - 180];
		*cos = -sinLUT[270 - angle];
	} else if(angle >= 90) {
		*sin =  sinLUT[180 - angle];
		*cos = -sinLUT[angle - 90];
	} else {
		*sin =  sinLUT[angle];
		*cos =  sinLUT[90 - angle];
	}
}
